#!/usr/bin/env bash
# ============================================================
# Build WASM workloads and regenerate main/wasm_bins.h
#
# Requirements:
#   Option A (recommended): wasi-sdk  https://github.com/WebAssembly/wasi-sdk
#     Set WASI_SDK_PATH to your wasi-sdk installation, e.g.:
#       export WASI_SDK_PATH=/opt/wasi-sdk
#
#   Option B: wabt (for WAT → WASM): https://github.com/WebAssembly/wabt
#     Provides wat2wasm; this script also ships .wat sources.
#
# Usage:
#   cd wasm
#   ./build.sh
#   # → regenerates ../main/wasm_bins.h
# ============================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
HEADER="$SCRIPT_DIR/../main/wasm_bins.h"

# ── Detect toolchain ──────────────────────────────────────────────────────
WASI_SDK_PATH="${WASI_SDK_PATH:-/opt/wasi-sdk}"
CLANG="$WASI_SDK_PATH/bin/clang"
CFLAGS="--target=wasm32-wasi --sysroot=$WASI_SDK_PATH/share/wasi-sysroot"
CFLAGS="$CFLAGS -O2 -nostdlib -Wl,--no-entry -Wl,--export=main"

if [ ! -x "$CLANG" ]; then
    echo "wasi-sdk not found at $WASI_SDK_PATH"
    echo "Trying system clang with explicit wasm32 target..."
    CLANG="clang"
    CFLAGS="--target=wasm32 -O2 -nostdlib -Wl,--no-entry -Wl,--export=main"
fi

echo "=== Building WASM workloads ==="
echo "Toolchain: $CLANG"

cd "$SCRIPT_DIR"

# ── Compile each workload ─────────────────────────────────────────────────
for SRC in cpu_stress mem_stress msg_stress; do
    echo "  Compiling $SRC.c → $SRC.wasm"
    $CLANG $CFLAGS -o "$SRC.wasm" "$SRC.c"
    SIZE=$(wc -c < "$SRC.wasm")
    echo "  → $SIZE bytes"
done

# ── Validate (optional, needs wasm-validate) ──────────────────────────────
if command -v wasm-validate &>/dev/null; then
    echo "Validating WASM modules..."
    for SRC in cpu_stress mem_stress msg_stress; do
        wasm-validate "$SRC.wasm" && echo "  $SRC.wasm: OK"
    done
fi

# ── Generate C header from binaries ──────────────────────────────────────
echo "Generating $HEADER"

python3 - <<PYEOF
import os, sys

script_dir = "$SCRIPT_DIR"
header_path = "$HEADER"

def to_c_array(name, data):
    lines = [f'static const uint8_t {name}[] = {{']
    for i in range(0, len(data), 16):
        chunk = data[i:i+16]
        hex_str = ', '.join(f'0x{b:02x}' for b in chunk)
        lines.append(f'    {hex_str},')
    lines.append('};')
    lines.append(f'static const uint32_t {name}_len = sizeof({name});')
    return '\n'.join(lines)

workloads = [
    ('wasm_cpu_stress', 'cpu_stress.wasm',
     'CPU stress: FNV-1a hash 1M iterations'),
    ('wasm_mem_stress', 'mem_stress.wasm',
     'Mem stress: 4KB scratchpad write/read 1000 rounds'),
    ('wasm_msg_stress', 'msg_stress.wasm',
     'Msg stress: ring-buffer producer/consumer 10000 iters'),
]

header = '/* Auto-generated by wasm/build.sh – DO NOT EDIT */\n'
header += '#pragma once\n#include <stdint.h>\n\n'

for arr_name, fname, desc in workloads:
    fpath = os.path.join(script_dir, fname)
    with open(fpath, 'rb') as f:
        data = f.read()
    header += f'/* {desc} ({len(data)} bytes) */\n'
    header += to_c_array(arr_name, data) + '\n\n'

with open(header_path, 'w') as f:
    f.write(header)

print(f'Written {header_path}')
PYEOF

echo ""
echo "Done. wasm_bins.h updated."
echo "Rebuild the ESP-IDF project: cd .. && idf.py build"
